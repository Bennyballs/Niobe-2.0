package org.niobe.model;

/**
 * Represents a direction an {@link Entity} can face.
 *
 * @author relex lawl
 */
public enum Direction {

	/**
	 * The north direction.
	 */
	NORTH,
	
	/**
	 * The north-east direction.
	 */
	NORTH_EAST,
	
	/**
	 * The east direction.
	 */
	EAST,
	
	/**
	 * The south-east direction.
	 */
	SOUTH_EAST,
	
	/**
	 * The south direction.
	 */
	SOUTH,
	
	/**
	 * The south-west direction.
	 */
	SOUTH_WEST,
	
	/**
	 * The west direction.
	 */
	WEST,
	
	/**
	 * The north-west direction.
	 */
	NORTH_WEST,
}
